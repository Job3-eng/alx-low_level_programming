#include "3-calc.h"

/**
 * op_add - addition
 * @a: first
 * @b: second
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: first
 * @b: second
 *
 * Return: matokeo
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: first
 * @b: second
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first
 * @b: second
 *
 * Return: remainder
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo
 * @a: first
 * @b: second
 *
 * Return: divisor
 */

int op_mod(int a, int b)
{
	return (a % b);
}
